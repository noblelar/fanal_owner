import {
  Links,
  Meta,
  Outlet,
  Scripts,
  ScrollRestoration,
} from "@remix-run/react";
import type { LinksFunction, MetaFunction } from "@remix-run/node";

import "./tailwind.css";
import { buildFanalMeta } from '~/utils/site-meta'

export const meta: MetaFunction = () => buildFanalMeta()

